import React, { useState, useEffect } from "react";
import { Card, Loader, FormField } from "../components/Index";
import { Link } from "react-router-dom";

const RenderCards = ({ data, title }) => {
  if (data?.length > 0) return data?.map((post, i) => <Card key={i} {...post} />);
  return (
    <h2 className="mt-2 font-bold uppercase text-[#6449ff] text-xl">{title}</h2>
  );
};

const Home = () => {
  const [loading, setLoading] = useState(false);
  const [posts, setPosts] = useState([]);
  const [searchtext, setSearchText] = useState("");
  const [searchResults, setSearchedResults] = useState(null);
  const [searchTimeout, setSearchTimeout] = useState(null);

  useEffect(() => {
    const fetchPosts = async () => {
      setLoading(true);
      try {
        const response = await fetch(
          "https://dalle-qgms.onrender.com/api/v1/post/",
          {
            method: "GET",
            headers: {
              "Content-Type": "application/json",
            },
          }
        );

        if (response.ok) {
          const result = await response.json();
          console.log(result);
          setPosts(result.data.reverse());
        }
      } catch (error) {
        alert(error);
      } finally {
        setLoading(false);
      }
    };
    fetchPosts();
  }, []);

  const handleSearchChange = (e) => {
    clearTimeout(searchTimeout);
    setSearchText(e.target.value);
    setSearchTimeout(
      setTimeout(() => {
        const searchResults = posts?.filter(
          (item) =>
            item.name.toLowerCase().includes(searchtext.toLocaleLowerCase()) ||
            item.prompt.toLowerCase().includes(searchtext.toLocaleLowerCase())
        );
        setSearchedResults(searchResults);
      }, 500)
    );
  };

  return (
    <section className="max-w-7xl mx-auto">
      <div>
        <h1 className="font-extrabold text-[#222328] text-lg">
          The Community ShowCase
        </h1>
        <p className="mt-2 text-[#666e75] text=[16px] max-w-[800px]">
          Download and browser through a collection of imaginative and visually
          stunning images generated by DALLE
        </p>
        <div className="flex items-center gap-2 ">
          <p className="mt-2 text-[#666e75] text-[16px] max-w-[700px]">
            Create and share your very own images with AIMAGEN community
          </p>
          <Link to="/create-post" className="flex items-center mt-2 underline text-[#6469ff] ">Create</Link>
        </div>
      </div>
      <div className="mt-16">
        <FormField
          type="text"
          labelName="Search Images"
          name="text"
          placeholder="Search Images by Prompt Keywords"
          value={searchtext}
          handleChange={handleSearchChange}
        />
      </div>
      <div className="mt-10 ">
        {loading ? (
          <div className="flex justify-center items-center">
            <Loader />
          </div>
        ) : (
          <>
            {searchtext && (
              <h2 className="font-medium text-[#666e75] text-xl mb-3 ">
                showing results for <span>{searchtext}</span>
              </h2>
            )}
            <div className="grid lg:grid-cols-4 sm:grid-cols-3 xs:grid-cols-2 grid-cols-1 gap-3">
              {searchtext ? (
                <RenderCards
                  data={searchResults}
                  title="No search results Found"
                />
              ) : (
                <RenderCards data={posts} title="No Posts Found" />
              )}
            </div>
          </>
        )}
      </div>
    </section>
  );
};

export default Home;
